<section class="about" id="about">
  <p>Project linkage is an experiment about human trust.</p>
  <p>
    A number of initial participants received coins with an individual QR code
    on the back. They have five basic tasks:
  </p>
  <ul>Hand this coin to a person they trust</ul>
  <ul>Have a conversation about their mutual relationship</ul>
  <ul>Scan the QR code and document the handover</ul>
  <ul>Write a short text about their conversation for everyone to read</ul>
  <ul>Request that the receiving person repeats these five steps</ul>
  <p>Thus, we create chains based on human trust. And tell our stories.</p>
